# Default to Bristol city centre when postcode is missing entirely
DEFAULT = (51.4545, -2.5879)


def _get_coords(postcode: str):
    """Return (lat, lon) for a postcode string, trying longest match first."""
    pc = postcode.upper().strip().replace(" ", "")
    # Try full district e.g. "BS16" or "NE1" before falling back to area "BS"/"NE"
    for pattern in (r'^([A-Z]{1,2}\d{1,2}[A-Z]?)', r'^([A-Z]{1,2}\d)', r'^([A-Z]{1,2})'):
        m = re.match(pattern, pc)
        if m:
            key = m.group(1)
            if key in POSTCODE_CENTROIDS:
                return POSTCODE_CENTROIDS[key]
    return None


def calculate_food_miles(postcode1: str, postcode2: str) -> float:
    """Return the straight-line distance in miles between two UK postcodes."""
    c1 = _get_coords(postcode1) if postcode1 else None
    c2 = _get_coords(postcode2) if postcode2 else None
    if not c1:
        c1 = DEFAULT
    if not c2:
        c2 = DEFAULT
    R = 3958.8  # Earth radius in miles
    lat1, lon1 = math.radians(c1[0]), math.radians(c1[1])
    lat2, lon2 = math.radians(c2[0]), math.radians(c2[1])
    dlat, dlon = lat2 - lat1, lon2 - lon1
    a = math.sin(dlat / 2) ** 2 + math.cos(lat1) * math.cos(lat2) * math.sin(dlon / 2) ** 2
    return round(R * 2 * math.asin(math.sqrt(a)), 1)
